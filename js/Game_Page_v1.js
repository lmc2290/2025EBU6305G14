function formatTime(totalSeconds) {
  const m = Math.floor(totalSeconds / 60).toString().padStart(2, '0');
  const s = (totalSeconds % 60).toString().padStart(2, '0');
  return `${m}:${s}`;
}

function startGame() {
  // Allow RLE mode to bypass the image upload requirement
  if (currentMode !== 'rle' && !originalImage) {
    alert("Please upload an image first!");
    return;
  }
  if (!isPlaying) {
    isPlaying = true;
    gameTimer = setInterval(() => {
      secondsElapsed++;
      timeDisplay.innerText = formatTime(secondsElapsed);

      // Real-time score decay for RLE mode
      if (currentMode === 'rle') {
        let currentScore = parseInt(scoreDisplay.innerText);
        if (currentScore > 0 && secondsElapsed % 2 === 0) { // Decay score every 2 seconds
          scoreDisplay.innerText = currentScore - 10;
        }
      }
    }, 1000);
  }
}

function pauseGame() {
  isPlaying = false;
  clearInterval(gameTimer);
}

function restartGame() {
  pauseGame();
  secondsElapsed = 0;
  timeDisplay.innerText = "00:00";
  if (originalImage || currentMode === 'rle') {
    switchMode(currentMode);
    if (currentMode !== 'rle') startGame();
  }
}