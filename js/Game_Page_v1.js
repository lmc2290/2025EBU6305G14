function switchMode(mode, event) {
  currentMode = mode;
  const data = MODE_DATA[mode];

  // Update Tab highlight
  document.querySelectorAll('.tab-btn').forEach(btn => btn.classList.remove('active'));
  if (event) event.target.classList.add('active');

  // Update basic text
  document.getElementById('page-title').innerText = data.title;
  document.getElementById('instruction-content').innerHTML = data.instructions;

  const rleVisualArea = document.getElementById('rle-visual-area');
  const canvasDOM = document.getElementById('gameCanvas');
  const uploadDOM = document.getElementById('upload-ui');
  const metricsBox = document.querySelector('.metrics-box');
  const bottomControls = document.querySelector('.game-panel > .controls');

  // Reset global timer when switching modes
  pauseGame();
  secondsElapsed = 0;
  timeDisplay.innerText = "00:00";

  if (mode === 'rle') {
    canvasDOM.style.display = 'none';
    uploadDOM.style.display = 'none';
    rleVisualArea.style.display = 'flex';

    // Hide general metrics and bottom controls for RLE
    if (metricsBox) metricsBox.style.display = 'none';
    if (bottomControls) bottomControls.style.display = 'none';

    showRLEStartScreen(); // Show start screen
  } else {
    rleVisualArea.style.display = 'none';

    // Restore general metrics and bottom controls
    if (metricsBox) metricsBox.style.display = 'flex';
    if (bottomControls) bottomControls.style.display = 'flex';

    if (originalImage) {
      canvasDOM.style.display = 'block';
      uploadDOM.style.display = 'none';
    } else {
      canvasDOM.style.display = 'none';
      uploadDOM.style.display = 'block';
    }

    resetCanvas();
    if (originalImage) {
      if (mode === 'compression') handleCompressionChange(80);
      if (mode === 'dct') handleDCTChange(0);
      startGame();
    }
  }
}

